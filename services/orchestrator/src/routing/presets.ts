const BY_ID = new Map<PresetId, Preset>(PRESETS.map((p) => [p.id, p]));

export function listPresets(): Preset[] {
  return PRESETS;
}

export function getPreset(id: PresetId): Preset | undefined {
  return BY_ID.get(id);
}

export function isPresetId(value: string): value is PresetId {
  return BY_ID.has(value as PresetId);
}

export const DEFAULT_PRESET_ID: PresetId = "balanced";
